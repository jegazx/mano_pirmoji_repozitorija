SELECT DIRBA_NUO, ASMENS_KODAS FROM DARBUOTOJAS WHERE DIRBA_NUO BETWEEN '2009-10-30' AND '2012-11-11'
SELECT VARDAS, SKYRIUS_ID, PROJEKTAS_ID FROM DARBUOTOJAS where SKYRIUS_ID IN (2, 3)

SELECT * FROM DARBUOTOJAS WHERE ASMENS_KODAS LIKE'_____12%'
SELECT * FROM PROJEKTAS WHERE PAVADINIMAS LIKE'_a%'
SELECT * FROM DARBUOTOJAS WHERE PAREIGOS ISNULL
SELECT VARDAS, PAVARDĖ, DIRBA_NUO, PAREIGOS FROM DARBUOTOJAS WHERE DIRBA_NUO >= date('2011-02-12') AND PAREIGOS='Programuotojas'
SELECT VARDAS, PAVARDĖ, SKYRIUS_ID, PROJEKTAS_ID 
FROM DARBUOTOJAS 
WHERE SKYRIUS_ID = 2 OR PROJEKTAS_ID = 1;
SELECT VARDAS FROM DARBUOTOJAS WHERE NOT VARDAS LIKE'A%'
SELECT VARDAS, PAVARDĖ, DIRBA_NUO FROM DARBUOTOJAS ORDER BY DIRBA_NUO
SELECT VARDAS, PAVARDĖ, DIRBA_NUO FROM DARBUOTOJAS ORDER BY DIRBA_NUO DESC
SELECT min(PROJEKTAS_ID), max(PROJEKTAS_ID) FROM DARBUOTOJAS
SELECT PROJEKTAS_ID, count(*) as ZMONIU_SK FROM DARBUOTOJAS GROUP BY PROJEKTAS_ID HAVING count(*)>3;
SELECT PROJEKTAS_ID, PAREIGOS, COUNT(*) as PROGRAMUOTOJU_SK
FROM DARBUOTOJAS
WHERE PAREIGOS = 'Programuotojas'
GROUP BY PROJEKTAS_ID, PAREIGOS;